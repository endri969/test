    private int findItemOrder(int item_id) {
        int order_id = -1;
        for (int i = 0; i < orders.size(); i++) {
            if (orders.get(i).getItem().getItemId() == item_id) {
                order_id = i;
            }
        }
        return order_id;
    }

    private void addOrderItem(int fromItemTabel) {
        if (fromItemTabel == 1) {

            Item item_order = cls.getItem(Integer.parseInt(
                    jTable2.getModel().getValueAt(
                            jTable2.getSelectedRow(), 0).toString()));

            int order_id = findItemOrder(item_order.getItemId());

            if (order_id == -1) {
                Order_details o = new Order_details();
                o.setItem(item_order);
                o.setQuantity(1);
                o.setTotal(item_order.getItemPrice());
                orders.add(o);
            } else {
                orders.get(order_id).setQuantity(orders.get(order_id).getQuantity() + 1);
                orders.get(order_id).setTotal(
                        orders.get(order_id).getItem().getItemPrice()
                        * orders.get(order_id).getQuantity());
            }
        } else if (fromItemTabel == 2) {
            int row = jTable3.getSelectedRow();
            orders.get(row).setQuantity(orders.get(row).getQuantity() + 1);
            orders.get(row).setTotal(
                    orders.get(row).getItem().getItemPrice()
                    * orders.get(row).getQuantity());
        } else if (fromItemTabel == 3) {
            int row = jTable3.getSelectedRow();
            int quantity = orders.get(row).getQuantity();
            if (quantity == 1) {
                orders.remove(row);
            } else if (row > 1) {

                orders.get(row).setQuantity(orders.get(row).getQuantity() - 1);
                orders.get(row).setTotal(
                        orders.get(row).getItem().getItemPrice()
                        * orders.get(row).getQuantity());
            }
        }
    }
