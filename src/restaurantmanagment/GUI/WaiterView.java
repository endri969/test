    private void displayOrdersTable() {
        grand_total = 0;
        model_order.getDataVector().removeAllElements();
        model_order.fireTableDataChanged();
        int count = 0;
        for (Order_details order : orders) {
            count++;
            model_order.addRow(new Object[]{count, order.getItem().getItemName(), order.getQuantity(),
                order.getItem().getItemPrice(), order.getTotal()

            });
            grand_total += order.getTotal();
        }
        txt_total.setText(String.format("%.2f L", grand_total));
        jTable3.setModel(model_order);
    }
