    private void displayData(){
        int a = 0;
        ArrayList<Category> categories = cls.getCategories();
        for (Category cat: categories) {
            cmb_Category.addItem(cat.getDescription()); //Name Column Value
            cmb_cat.addItem(cat.getDescription());
            IDHolder.put(a, cat.getCategory_id()); //ID Column Value
            a++;
        }
        ArrayList<Item> items = cls.getItems();
        model = new DefaultTableModel(new Object[]{"ID", "Item", "Price"}, 0);
        for (Item item : items) {
            model.addRow(new Object[]{item.getItemId(),item.getItemName(),
                item.getItemPrice()

            });
            
        }
        jTable2.setModel(model);
        filter("");
    }
