    private void btn_update_itemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_update_itemActionPerformed

        int id = Integer.parseInt(txt_itmId.getText());
        int category_id = cmb_cat.getSelectedIndex()+1;
        String item_name = txt_itmName.getText();
        double price = Double.parseDouble(txt_itmPrice.getText());
        
        int row = cls.updateItem(id, item_name, category_id, price);
        if (row == 1) {
            txt_msg.setText("Item Updated Succesfuly");
            displayData();
        } else {
            txt_msg.setText("Can not update item");
        }
        
    }//GEN-LAST:event_btn_update_itemActionPerformed

